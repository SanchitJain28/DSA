function InsertionSort(array) {
  for (let i = 1; i < array.length; i++) {
    let key = array[i];
    let j = i - 1;

    for (; j >= 0 && array[j] > key; ) {
      array[j + 1] = array[j];
      j--;
    }

    array[j + 1] = key;
  }
  return array;
}
